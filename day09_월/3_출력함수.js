/*
  변수를 사용하지 않고, 데이터(리터럴)를 직접 출력하는 방법들
*/

// 1. console.log() : 개발자 도구 콘솔에 데이터를 출력
// 개발자가 코드의 실행 결과를 확인하기 위해 사용합니다.
console.log("--- 1. console.log() 예제 ---");
console.log("콘솔 화면에 출력되는 글자입니다.");
console.log(12345); // 숫자도 직접 출력할 수 있습니다.
console.log(true); // 불리언 값도 가능합니다.
console.log([1, "하나", 2, "둘"]); // 배열도 직접 넣을 수 있습니다.


// 2. alert() : 팝업창으로 데이터를 출력
// 사용자에게 간단한 정보를 알릴 때 사용합니다.
console.log("--- 2. alert() 예제 ---");
// 아래 코드의 주석(//)을 지우고 실행하면 팝업창이 뜹니다.
// alert("팝업창으로 알리는 메시지입니다!");


// 3. document.write() : 웹 페이지에 직접 내용을 쓰는 함수
// 페이지가 로딩될 때 내용을 추가하는 오래된 방식입니다.
// 참고: 페이지 로딩이 끝난 후에 사용하면 기존 내용을 모두 지우고 새로 쓰기 때문에 주의해야 합니다.
console.log("--- 3. document.write() 예제 ---");
document.write("<h2>document.write()로 추가한 제목</h2>");
document.write("<p>웹 페이지에 직접 내용이 추가됩니다.</p>");


// 4. document.body.innerHTML = : body 전체의 내용을 교체
// 웹 페이지의 <body> 태그 안의 모든 내용을 지정한 HTML로 바꿔버립니다.
